"""Промпт LLM-экстрактора экспериментов из сырого текста документа.

Фаза 2 ингеста: из чанка документа вытаскиваем атомарные «эксперименты» —
связки материал × режим × свойство × значение × вывод. Только явно описанное.
"""

SYSTEM = (
    "Ты — инженер-исследователь горно-металлургической отрасли. Из фрагмента "
    "научного документа извлекаешь описанные эксперименты и измерения. "
    "Отвечай СТРОГО валидным JSON без пояснений. "
    "Не выдумывай: включай только то, что явно присутствует в тексте. "
    "ВАЖНО: значения полей 'material', 'mode', 'property', 'conclusion' пиши "
    "по-русски даже если исходный текст на английском — переводи термины. "
    "Химические символы (Ni, Cu, SO2), формулы, номера ГОСТ/стандартов и "
    "единицы измерения (°C, MPa, mg/l) оставляй как есть."
)


def build_prompt(chunk_text, known_materials=None, known_properties=None):
    parts = [
        "Формат ответа:",
        '{"experiments": [',
        '  {"material": "…", "mode": "…", "property": "…", "value": число|null,',
        '   "unit": "…"|null, "conclusion": "…", "confidence": 0.0-1.0}',
        "]}",
        "",
        "Правила:",
        "- material — материал/руда/сплав/реагент (по-русски: «медный концентрат», "
        "«никелевый штейн», «серная кислота», а не «copper concentrate»);",
        "- mode — режим/технология/условия по-русски с числами: температура, "
        "концентрация, давление, pH, расход, плотность тока;",
        "- property — измеряемое свойство по-русски («извлечение», «прочность», "
        "«выход», «содержание примеси»);",
        "- value/unit — числовой результат и единица (единица без перевода: °C, MPa);",
        "- conclusion — краткий вывод одной фразой по-русски;",
        "- confidence — насколько уверенно эксперимент вычленяется из текста;",
        "- если экспериментов нет — верни {\"experiments\": []}.",
        "",
    ]
    if known_materials:
        parts.append("Известные материалы (используй именно эти русские названия, "
                     "НЕ коды типа MAT-XXX): " + ", ".join(known_materials[:40]))
    if known_properties:
        parts.append("Известные свойства (используй именно эти русские названия, "
                     "НЕ коды типа PROP-XXX): " + ", ".join(known_properties[:30]))
    parts.append("Если из текста не удаётся однозначно определить материал — "
                 "оставь поле пустой строкой, дальше сработает словарный NER "
                 "по контексту документа.")
    parts.append("")
    parts.append("ТЕКСТ:")
    parts.append(chunk_text[:1800])
    parts.append("")
    parts.append("JSON:")
    return "\n".join(parts)
