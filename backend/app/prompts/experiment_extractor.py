"""Промпт LLM-экстрактора экспериментов из сырого текста документа.

Фаза 2 ингеста: из чанка документа вытаскиваем атомарные «эксперименты» —
связки материал × режим × свойство × значение × вывод. Только явно описанное.
"""

SYSTEM = (
    "Ты — инженер-исследователь горно-металлургической отрасли. Из фрагмента "
    "научного документа извлекаешь описанные эксперименты и измерения. "
    "Отвечай СТРОГО валидным JSON без пояснений. "
    "Не выдумывай: включай только то, что явно присутствует в тексте."
)


def build_prompt(chunk_text, known_materials=None, known_properties=None):
    parts = [
        "Формат ответа:",
        '{"experiments": [',
        '  {"material": "…", "mode": "…", "property": "…", "value": число|null,',
        '   "unit": "…"|null, "conclusion": "…", "confidence": 0.0-1.0}',
        "]}",
        "",
        "Правила:",
        "- material — материал/руда/сплав/реагент; mode — режим/технология/условия "
        "(с числами: температура, концентрация, давление, pH, расход, плотность тока);",
        "- property — измеряемое свойство; value/unit — числовой результат, если указан;",
        "- conclusion — краткий вывод одной фразой;",
        "- confidence — насколько уверенно эксперимент вычленяется из текста;",
        "- если экспериментов нет — верни {\"experiments\": []}.",
        "",
    ]
    if known_materials:
        parts.append("Известные коды материалов (подгоняй при совпадении): "
                     + ", ".join(known_materials[:40]))
    if known_properties:
        parts.append("Известные свойства: " + ", ".join(known_properties[:30]))
    parts.append("")
    parts.append("ТЕКСТ:")
    parts.append(chunk_text[:1800])
    parts.append("")
    parts.append("JSON:")
    return "\n".join(parts)
