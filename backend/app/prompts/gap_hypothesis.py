"""Промпт генерации гипотезы про пустую ячейку матрицы пробелов.

История: старая версия давала одинаковый шаблонный ответ на разные ячейки,
потому что не передавался конкретный контекст (что вообще известно про
материал/процесс, какие клетки заполнены рядом). Теперь промпт требует:
1) Опираться на реальные свойства материала (химсостав/семейство);
2) Учитывать соседей — какие процессы уже применяли к этому материалу;
3) Не сочинять общее — либо конкретная причина, либо «данных мало».
"""

SYSTEM = (
    "Ты — научный ассистент-металлург. На вход подаётся конкретная комбинация "
    "(материал, процесс, свойство), для которой в базе НЕТ экспериментов, а "
    "также известные соседние комбинации (то, что УЖЕ исследовано).\n\n"
    "Твоя задача — сформулировать НЕобщую гипотезу почему именно эта клетка "
    "пуста, опираясь на:\n"
    "- физико-химические особенности материала (если известны — сплав, "
    "концентрат, раствор, металл);\n"
    "- ограничения процесса (температурный режим, среда, реакционность);\n"
    "- что уже сделано с этим материалом в соседних клетках (какие процессы, "
    "какие свойства мерили);\n"
    "- экономический смысл (сложность / стоимость).\n\n"
    "ЗАПРЕЩЕНО: шаблонные фразы «может быть нестабилен», «сложно провести», "
    "«нужно исследовать». Если конкретной причины нет — прямо напиши "
    "«недостаточно данных для гипотезы, нужны рефересные эксперименты по "
    "материалу-аналогу X».\n\n"
    "Формат ответа: 3–5 предложений на русском, без вводных, "
    "начиная с сути гипотезы."
)


def build_prompt(material, process, property_, neighbors=None, material_facts=None):
    """
    material         — {code, display_name, family?, base_element?}
    process          — str (Mode.category или display_name)
    property_        — {code, display_name, unit?}
    neighbors        — list[str] соседей: «Материал X × процесс Y (n=3)»
    material_facts   — list[str] что известно про материал: «использовался для Z»
    """
    mat_name = material.get("display_name") or material.get("code")
    is_provisional = str(mat_name) == str(material.get("code"))

    lines = [
        "ПУСТАЯ ЯЧЕЙКА:",
        f"  Материал: {mat_name}"
        + (f" (код {material.get('code')})" if not is_provisional else ""),
        f"  Процесс: {process}",
        f"  Свойство: {property_.get('display_name') or property_.get('code')}",
    ]
    if material.get("family") or material.get("base_element"):
        parts = []
        if material.get("family"):
            parts.append(f"семейство {material['family']}")
        if material.get("base_element"):
            parts.append(f"базовый элемент {material['base_element']}")
        lines.append(f"  Тип: {', '.join(parts)}")

    if is_provisional:
        lines.append("")
        lines.append(
            "⚠ У материала нет читабельного названия — только автогенерированный "
            "код. Прямо укажи это в гипотезе: LLM-извлечение из документов "
            "поймало сущность, но не дало ей осмысленное имя. Гипотеза должна "
            "опираться на соседей ниже."
        )

    if material_facts:
        lines.append("")
        lines.append("ЧТО ИЗВЕСТНО ПРО ЭТОТ МАТЕРИАЛ:")
        for f in material_facts[:5]:
            lines.append(f"  - {f}")

    if neighbors:
        lines.append("")
        lines.append("СОСЕДНИЕ КЛЕТКИ (уже исследованы):")
        for n in neighbors[:8]:
            lines.append(f"  - {n}")

    lines.append("")
    lines.append(
        "Дай ОДНУ конкретную гипотезу (или честно скажи «недостаточно данных»). "
        "Соотнеси её с соседями: почему для этого материала процесс X делали, "
        "а именно этот процесс — нет. 3–5 предложений."
    )
    return "\n".join(lines)
